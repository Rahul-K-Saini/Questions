    public static int findSpecialInteger(int[] arr) {
        int freq = 1;
        int max = 0;
        int ans = 0;
        for(int i=0; i<arr.length-1; i++){
            if(arr[i]==arr[i+1]){
                freq++;
            }
            else {
                if(freq>max){
                    max = freq;
                    ans = arr[i];
                }
                freq = 1;
            }
        }
        if(freq>max) ans = arr[arr.length-1];
        if(ans > 0) return ans;
        return arr[0];

    }
